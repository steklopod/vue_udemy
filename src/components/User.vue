<template>
  <div class="component">
    <h1>The User Component</h1>

    <button @click="changeName">Change my name</button>

    <hr>
    <div class="row">

      <div class="col-xs-12 col-sm-6">
        <app-user-detail
          :name="name"
          @nameWasReset="name = $event"
          :resetFn="resetName"
          :userAge="age"
        ></app-user-detail>
      </div>

      <div class="col-xs-12 col-sm-6">
        <app-user-edit
          :userAge="age"
          @ageWasEdited="age= $event"
        ></app-user-edit>
      </div>

    </div>
  </div>
</template>

<script>
  import UserDetail from './UserDetail.vue'
  import UserEdit from './UserEdit.vue'

  export default {
    data () {
      return {
        age: 29,
        name: 'Dima'
      }
    },

    methods: {
      changeName () {
        this.name = 'Vasya'
      },
      resetName () {
        this.name = 'Dima'
      }

    },

    components: {
      appUserDetail: UserDetail,
      appUserEdit: UserEdit
    }
  }
</script>

<style scoped>
  div {
    background-color: lightblue;
  }
